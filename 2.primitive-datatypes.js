// number
let marks = 85;
let temperature = 29.5;
console.log(marks);
console.log(temperature);

// string
let city = "Bangalore";
let country = 'India';
console.log(city);
console.log(country);

// template literal (string interpolation)
let intro = `I scored ${marks} marks. I live in ${city}, which is in ${country}, and today’s temperature is ${temperature}°C.`;
console.log(intro);

// boolean
let isCompleted = true;
let isLoggedIn = false;
console.log(isCompleted);
console.log(isLoggedIn);

// undefined
let futurePlan;
console.log(futurePlan);  // undefined

// null
let laptop = null;
console.log(laptop);

// symbol
let sym2 = Symbol("Backend Developer");
console.log(sym2);
